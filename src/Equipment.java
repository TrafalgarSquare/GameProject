
public class Equipment {

	public boolean weaponBought = false;
	public boolean armorBought = false;
	
	
	public Equipment(){
		
	}
	
	
	public boolean isWeaponBought() {
		return weaponBought;
	}

	public void setWeaponBought(boolean weaponBought) {
		this.weaponBought = weaponBought;
	}

	public boolean isArmorBought() {
		return armorBought;
	}

	public void setArmorBought(boolean armorBought) {
		this.armorBought = armorBought;
	}

	
}
